const { GraphQLServer } = require('graphql-yoga')
const connectDB = require('./config/db')

const typeDefs = `
  type Query {
    hello(name: String): String!
  }
`

const resolvers = {
  Query: {
    hello: (_, { name }) => `Hello ${name || 'World'}`,
  },
}

const server = new GraphQLServer({ typeDefs, resolvers })

server.start(connectDB())
